#!/bin/bash

show_help() {
    echo "Использование: $0 <метод_очистки>"
    echo "Методы очистки:"
    echo "1 - Удаление по лог-файлу"
    echo "2 - Удаление по дате создания"
    echo "3 - Удаление по маске имени"
    echo ""
    echo "Примеры:"
    echo "$0 1  # Очистка по лог-файлу"
    echo "$0 2  # Очистка по дате создания"
    echo "$0 3  # Очистка по маске имени"
}

check_root() {
    if [ "$(id -u)" -ne 0 ]; then
        echo "Этот скрипт требует прав root для полного доступа к файловой системе" >&2
        exit 1
    fi
}
